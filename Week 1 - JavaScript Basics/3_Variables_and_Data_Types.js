console.log("1. Variables");
console.log("   - Variables are used to store data values.");
console.log("   - In JavaScript, you can declare variables using 'var', 'let', or 'const'.");
console.log("   Example:");
console.log("   let name = 'Shivek'; // Declares a variable 'name' and assigns the value 'Shivek'.");
console.log("   const age = 25; // Declares a constant variable 'age' with the value 25.");
console.log("   var city = 'Ludhiana'; // Declares a variable 'city' using 'var'.");

console.log("\n2. Data Types");
console.log("   JavaScript has several basic data types:");

console.log("\n   a. String");
console.log("      - Represents text or a sequence of characters.");
console.log("      - Strings are enclosed in single or double quotes.");
console.log("      Example:");
console.log("      let greeting = 'Hello, World!';");

console.log("\n   b. Number");
console.log("      - Represents numeric values, including integers and floating-point numbers.");
console.log("      Example:");
console.log("      let price = 99.99; // A floating-point number.");
console.log("      let quantity = 5; // An integer.");

console.log("\n   c. Boolean");
console.log("      - Represents a logical entity and can have only two values: 'true' or 'false'.");
console.log("      Example:");
console.log("      let isActive = true; // A boolean value.");

console.log("\n   d. Undefined");
console.log("      - A variable that has been declared but not assigned a value.");
console.log("      Example:");
console.log("      let score; // The variable 'score' is declared but has no value, so it is 'undefined'.");

console.log("\n   e. Null");
console.log("      - Represents the intentional absence of any value.");
console.log("      Example:");
console.log("      let result = null; // The variable 'result' has no value (null).");

console.log("\n   f. Object");
console.log("      - Represents a complex data structure, like an array or an object with properties.");
console.log("      Example:");
console.log("      let person = { name: 'Shivek', age: 25, city: 'Ludhiana' };");

console.log("\n   g. Array");
console.log("      - An ordered collection of items.");
console.log("      Example:");
console.log("      let colors = ['red', 'green', 'blue']; // An array of strings.");

console.log("\n3. Dynamic Typing");
console.log("   - JavaScript is dynamically typed, meaning you don't have to declare the data type of a variable explicitly.");
console.log("   - The data type is automatically determined based on the assigned value.");
console.log("   Example:");
console.log("   let message = 'Hello'; // 'message' is a string.");
console.log("   message = 100; // Now 'message' is a number.");
